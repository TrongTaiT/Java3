    private void editMode(boolean state) {
        lblEditState.setText("Edit: " + (state ? "ON" : "OFF"));

        txtTiengAnh.setEditable(state);
        txtTinHoc.setEditable(state);
        txtGiaoDucTC.setEditable(state);
    }

    private void resetForm() {
        resetColorForFields();

        txtMaSVTim.setText("");
        lblHoTen.setText("");
        lblMaSV.setText("");
        txtTiengAnh.setText("");
        txtTinHoc.setText("");
        txtGiaoDucTC.setText("");
        lblDiemTB.setText("");

        tblStudents.clearSelection();
        currentIndex = tblStudents.getSelectedRow();
        editModeState = true;
        editMode(editModeState);
    }

    private void resetColorForFields() {
        txtTiengAnh.setBackground(Color.white);
        txtTinHoc.setBackground(Color.white);
        txtGiaoDucTC.setBackground(Color.white);
    }

    private void showDetail() {
        editModeState = false;
        editMode(editModeState);
        resetColorForFields();
        tblStudents.setRowSelectionInterval(currentIndex, currentIndex);

        Student temp = list.get(currentIndex);
        lblHoTen.setText(temp.getHoTen());
        lblMaSV.setText(temp.getMaSV());
        txtTiengAnh.setText(temp.getTiengAnh() + "");
        txtTinHoc.setText(temp.getTinHoc() + "");
        txtGiaoDucTC.setText(temp.getGDTC() + "");
        lblDiemTB.setText(temp.getDTB() + "");
    }

    private boolean checkForm() {
        StringBuilder sb = new StringBuilder();

        checkGradeField(sb, txtTiengAnh, txtTinHoc, txtGiaoDucTC);

        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb, "Thông báo lỗi", 0);
            return false;
        }

        return true;
    }

    private void checkGradeField(StringBuilder sb, JTextField... fields) {
        Color errColor = new Color(236, 100, 75);
        for (JTextField txt : fields) {
            try {
                float grade = Float.parseFloat(txt.getText());
                if (grade > 10 || grade < 0) {
                    sb.append("Điểm phải lớn hơn 0 và nhỏ hơn 10");
                    txt.setBackground(errColor);
                    return;
                }
            } catch (Exception e) {
                sb.append("Điểm phải là số");
                txt.setBackground(errColor);
                return;
            }

            txt.setBackground(new Color(38, 194, 129));
        }
    }

    private Student getStudentFromFormInput() {
        Student student = new Student();
        student.setMaSV(lblMaSV.getText());
        student.setHoTen(lblHoTen.getText());
        student.setTiengAnh(Float.parseFloat(txtTiengAnh.getText()));
        student.setTinHoc(Float.parseFloat(txtTinHoc.getText()));
        student.setGDTC(Float.parseFloat(txtGiaoDucTC.getText()));
        return student;
    }

    private void updateStudentGrade() {
        if (!checkForm()) {
            return;
        }

        Student tempStudent = getStudentFromFormInput();
        if (StudentGradeDAO.save(tempStudent) > 0) {
            fillToTable();
            resetForm();
            JOptionPane.showMessageDialog(this, "Đã cập nhật thông tin thành công");
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thông tin thất bại");
        }
    }
