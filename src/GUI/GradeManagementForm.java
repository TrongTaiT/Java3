    private void fillToTable() {
        list = StudentGradeDAO.getAll();

        // order list by DTB - descending
        Comparator<Student> comp = (Student o1, Student o2) -> {
            if (o1.getDTB() == o2.getDTB()) {
                return 0;
            } else if (o1.getDTB() < o2.getDTB()) {
                return 1;
            } else {
                return -1;
            }
        };
        Collections.sort(list, comp);

        DefaultTableModel model = (DefaultTableModel) tblStudents.getModel();
        model.setRowCount(0);

        for (Student x : list) {
            Object[] row = {x.getMaSV(), x.getHoTen(), x.getTiengAnh(), x.getTinHoc(),
                x.getGDTC(), x.getDTB()};
            model.addRow(row);
        }
        tblStudents.setModel(model);
    }

